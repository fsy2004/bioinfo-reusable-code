def fig_domain_maps(df: pd.DataFrame, methods: dict, outstem: Path) -> None:
    """空间散点: 行=方法(含 ground truth), 列=切片。看域是否空间连续、跨切片是否对齐。"""
    slides = sorted(df["slide"].unique())
    names = ["Ground truth"] + list(methods)
    labsets = [df["domain_true"].values] + [methods[m] for m in methods]
    fig, axes = plt.subplots(len(names), len(slides),
                             figsize=(NATURE_W2, 2.0 * len(names)), squeeze=False)
    for r, (nm, lab) in enumerate(zip(names, labsets)):
        cats = sorted(set(lab))
        cmap = dict(zip(cats, pal(len(cats), "okabe_ito")))
        for c, sid in enumerate(slides):
            ax = axes[r][c]
            m = df["slide"].values == sid
            ax.scatter(df["x"].values[m], df["y"].values[m],
                       c=[cmap[v] for v in np.asarray(lab)[m]], s=6, linewidths=0)
            ax.set_xticks([]); ax.set_yticks([]); ax.set_aspect("equal")
            for sp in ax.spines.values():
                sp.set_visible(True)
            if r == 0:
                ax.set_title(sid, fontsize=9)
            if c == 0:
                ax.set_ylabel(nm, fontsize=8)
    fig.suptitle("Spatial domain assignment across slides", fontsize=11, fontweight="bold")
    fig.tight_layout()
    save_fig(fig, outstem)
    plt.close(fig)


def fig_metric_lollipop(metrics: pd.DataFrame, outstem: Path) -> None:
    """棒棒糖图比较各方法的 FIDE / heuristic / ARI(越高越好)与 JSD(越低越好)。"""
    panels = [("FIDE", "higher = more continuous domains", False),
              ("heuristic", "higher = continuous + balanced", False),
              ("ARI", "higher = closer to ground truth", False),
              ("JSD", "lower = shared across slides", True)]
    fig, axes = plt.subplots(1, len(panels), figsize=(NATURE_W2, 2.6), sharey=True)
    colors = pal(len(metrics), "npg")
    ypos = np.arange(len(metrics))[::-1]
    for ax, (col, sub, lower_better) in zip(axes, panels):
        v = metrics[col].values
        ax.hlines(ypos, 0, v, color="#BBBBBB", linewidth=1.6, zorder=1)
        ax.scatter(v, ypos, s=70, c=colors, zorder=2, edgecolors="white", linewidths=0.8)
        ax.set_title(col + ("  (lower better)" if lower_better else "  (higher better)"),
                     fontsize=8)
        ax.set_xlabel(sub, fontsize=6)
        ax.set_xlim(0, max(float(np.max(v)) * 1.25, 1e-3))
    axes[0].set_yticks(ypos)
    axes[0].set_yticklabels(metrics["method"].tolist(), fontsize=8)
    fig.suptitle("Baseline spatial-domain callers scored with the Novae metrics",
                 fontsize=10, fontweight="bold")
    fig.tight_layout()
    save_fig(fig, outstem)
    plt.close(fig)


def fig_composition_heatmap(df: pd.DataFrame, methods: dict, outstem: Path) -> None:
    """热图: 每方法下 域 x 切片 的细胞占比。列间越一致 = 域越可跨切片迁移(JSD 的可视化)。"""
    slides = sorted(df["slide"].unique())
    fig, axes = plt.subplots(1, len(methods), figsize=(NATURE_W2, 2.8), squeeze=False)
    for ax, (nm, lab) in zip(axes[0], methods.items()):
        cats = sorted(set(lab))
        M = np.zeros((len(cats), len(slides)))
        for c, sid in enumerate(slides):
            v = pd.Series(np.asarray(lab)[df["slide"].values == sid]).value_counts(normalize=True)
            M[:, c] = [v.get(k, 0.0) for k in cats]
        im = ax.imshow(M, cmap=CMAP_CONT, aspect="auto", vmin=0, vmax=max(0.6, M.max()))
        ax.set_xticks(range(len(slides))); ax.set_xticklabels(slides, fontsize=7, rotation=45)
        ax.set_yticks(range(len(cats))); ax.set_yticklabels(cats, fontsize=7)
        ax.set_title(nm, fontsize=8)
        for sp in ax.spines.values():
            sp.set_visible(False)
    fig.colorbar(im, ax=axes[0].tolist(), shrink=0.8, label="fraction of cells")
    fig.suptitle("Domain composition per slide (column-wise consistency = transferability)",
                 fontsize=10, fontweight="bold", y=1.08)
    save_fig(fig, outstem)
    plt.close(fig)


