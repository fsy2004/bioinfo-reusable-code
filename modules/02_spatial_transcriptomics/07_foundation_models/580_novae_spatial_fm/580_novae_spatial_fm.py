def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--csv", default=str(EXAMPLE),
                    help="细胞表: cell_id,slide,x,y[,domain_true],<基因列...>")
    ap.add_argument("--slide-key", default="slide")
    ap.add_argument("--n-domains", type=int, default=4)
    ap.add_argument("--k-niche", type=int, default=20, help="niche 平滑用的空间近邻数")
    ap.add_argument("--k-graph", type=int, default=6, help="FIDE 邻接图的近邻数")
    ap.add_argument("--run-novae", action="store_true", help="尝试真正调用 Novae(需安装 + 真实基因名)")
    ap.add_argument("--novae-model", default="prism-oncology/novae-human-0")
    ap.add_argument("--novae-level", type=int, default=7)
    ap.add_argument("--outdir", default=str(ROOT / "results"))
    ap.add_argument("--assets", default=str(ROOT / "assets"))
    ap.add_argument("--seed", type=int, default=SEED)
    a = ap.parse_args()

    outdir, assets = Path(a.outdir), Path(a.assets)
    outdir.mkdir(parents=True, exist_ok=True); assets.mkdir(parents=True, exist_ok=True)
    np.random.seed(a.seed)
    set_pub_style(base_size=9)

    csv = Path(a.csv)
    if csv == EXAMPLE and not csv.exists():
        print(f"[580] 示例数据不存在, 生成 -> {csv}")
        make_example(csv, seed=a.seed)
    print(f"[580] Step 1 读入 {csv}")
    df = load_table(csv)
    META_COLS = ("cell_id", a.slide_key, "x", "y", "domain_true", "cell_type_true")
    meta = [c for c in META_COLS if c in df.columns]
    gene_cols = [c for c in df.columns if c not in meta]
    slide = df[a.slide_key].astype(str).values
    xy = df[["x", "y"]].to_numpy(float)
    X = _normalize_log(df[gene_cols].to_numpy(float))
    print(f"       {len(df)} cells · {len(gene_cols)} genes · {len(set(slide))} slides")

    print("[580] Step 2 构建切片内空间 kNN 图(FIDE 用)")
    edges_by_slide = {}
    for sid in sorted(set(slide)):
        m = slide == sid
        i, j = _spatial_edges(xy[m], k=a.k_graph)
        edges_by_slide[sid] = (i, j)

    print("[580] Step 3 基线: expression-only / niche-smoothing")
    methods: dict[str, np.ndarray] = {
        "Expression-only (PCA+KMeans)": baseline_expression_only(X, a.n_domains, a.seed),
        "Niche smoothing (kNN)": baseline_niche_smoothing(
            X, xy, slide, a.n_domains, k=a.k_niche, seed=a.seed, prefix="N"),
        "Niche smoothing + per-slide z": baseline_niche_smoothing(
            X, xy, slide, a.n_domains, k=a.k_niche, seed=a.seed,
            center_per_slide=True, prefix="Z"),
    }

    if a.run_novae:
        print("[580] Step 3b Novae(守卫式)")
        r = run_novae(df, gene_cols, a.slide_key, a.n_domains, a.novae_model, a.novae_level)
        for k, v in r.items():
            if k != "labels":
                print(f"       {k}: {v}")
        if r.get("status") == "ok":
            methods["Novae (zero-shot)"] = np.array(r["labels"])
        with open(outdir / "580_novae_status.json", "w", encoding="utf-8") as fh:
            json.dump({k: v for k, v in r.items() if k != "labels"}, fh, indent=1, ensure_ascii=False)
    else:
        print("[580] Novae 路径未请求(--run-novae), 仅跑基线")

    print("[580] Step 4 打分(FIDE / JSD / heuristic / ARI, 指标复刻自上游 monitor/eval.py)")
    from sklearn.metrics import adjusted_rand_score
    rows = []
    for nm, lab in methods.items():
        nc = len(set(lab))
        rows.append({
            "method": nm,
            "n_domains": nc,
            "FIDE": round(mean_fide(lab, edges_by_slide, slide, a.n_domains), 4),
            "JSD": round(jensen_shannon_divergence(lab, slide), 4),
            "heuristic": round(heuristic(lab, edges_by_slide, slide, a.n_domains), 4),
            "ARI": round(adjusted_rand_score(df["domain_true"].values, lab), 4)
            if "domain_true" in df.columns else np.nan,
            # 诊断列: 若某方法 ARI_celltype 高而 ARI(域) 低, 说明它只还原了细胞类型、
            # 根本没抓到 niche —— expression-only 地板正应如此。
            "ARI_celltype": round(adjusted_rand_score(df["cell_type_true"].values, lab), 4)
            if "cell_type_true" in df.columns else np.nan,
        })
    metrics = pd.DataFrame(rows)
    print(metrics.to_string(index=False))
    metrics.to_csv(outdir / "580_metrics.csv", index=False)
    lab_out = df[meta].copy()
    for nm, lab in methods.items():
        lab_out[nm] = lab
    lab_out.to_csv(outdir / "580_domain_labels.csv", index=False)

    print("[580] Step 5 出图")
    fig_domain_maps(df, methods, assets / "580_domain_maps")
    fig_metric_lollipop(metrics, assets / "580_metric_comparison")
    fig_composition_heatmap(df, methods, assets / "580_slide_composition")

    with open(outdir / "580_summary.json", "w", encoding="utf-8") as fh:
        json.dump({"input": str(csv), "n_cells": len(df), "n_genes": len(gene_cols),
                   "slides": sorted(set(slide)), "metrics": rows,
                   "novae_attempted": bool(a.run_novae), "seed": a.seed},
                  fh, indent=1, ensure_ascii=False)
    print(f"[580] 完成 · results -> {outdir} · figures -> {assets}")


