def run_novae(df: pd.DataFrame, gene_cols: list[str], slide_key: str,
              n_domains: int, model_name: str, level: int) -> dict:
    """真正跑 Novae。任一前置条件不满足 -> 返回 skipped + 真实的补救命令, 不伪造结果。

    调用序列已逐个在上游源码里核对(文件:行见模块 docstring):
        novae.spatial_neighbors(adata, slide_key=...)          utils/build.py:35
        novae.Novae.from_pretrained("prism-oncology/novae-human-0")  model.py:309
        model.compute_representations(adata, zero_shot=True)   model.py:379
        model.assign_domains(adata, level=..., n_domains=...)  model.py:539
    注: 上游 assign_domains 在 zero-shot 下会 log.warning 建议改用 resolution=(见
    model.py:588); 本封装保留 level/n_domains 路径, 需要时用 --novae-level 调。
    """
    try:
        import novae
    except ImportError as e:
        return {"status": "skipped",
                "reason": f"novae 未安装 ({e.name}); 需要 Python>=3.11 与 torch-geometric",
                "install": "pip install novae"}
    try:
        import anndata as ad
        A = ad.AnnData(df[gene_cols].to_numpy(dtype="float32"))
        A.var_names = gene_cols
        A.obs_names = df["cell_id"].astype(str).values
        A.obs[slide_key] = pd.Categorical(df[slide_key].values)
        A.obsm["spatial"] = df[["x", "y"]].to_numpy(dtype="float32")

        novae.spatial_neighbors(A, slide_key=slide_key)
        model = novae.Novae.from_pretrained(model_name)
        model.compute_representations(A, zero_shot=True)
        key = model.assign_domains(A, level=level, n_domains=n_domains)
        return {"status": "ok", "novae_version": getattr(novae, "__version__", "?"),
                "domain_key": key, "labels": A.obs[key].astype(str).tolist()}
    except Exception as e:                     # 权重下载失败 / 基因名非真实 symbol 等
        return {"status": "failed",
                "reason": f"{type(e).__name__}: {e}",
                "note": ("Novae 依赖真实 human/mouse 基因 symbol 做基因嵌入, 合成数据的 G000 "
                         "这类占位名不可用; 且 from_pretrained 需能访问 HuggingFace Hub。"
                         "请传 --csv 你的真实数据, 并确认网络可达。")}


