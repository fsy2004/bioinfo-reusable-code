def _entropy(p: np.ndarray) -> float:
    """Shannon entropy(上游 entropy(), EPS 与上游 Nums.EPS 同量级)。"""
    return float(-(p * np.log2(p + 1e-8)).sum())


def fide_score(labels: np.ndarray, edges: tuple[np.ndarray, np.ndarray],
               n_classes: int | None = None) -> float:
    """F1-score of intra-domain edges(FIDE): 边两端标签当作 y_true/y_pred 求逐类 F1 后平均。"""
    from sklearn.metrics import f1_score
    i, j = edges
    f1 = f1_score(labels[i], labels[j], average=None)
    if n_classes is None:
        return float(f1.mean())
    return float(np.pad(f1, (0, max(0, n_classes - len(f1)))).mean())


def mean_fide(labels: np.ndarray, edges_by_slide: dict, slide: np.ndarray,
              n_classes: int) -> float:
    vals = []
    for sid, (i, j) in edges_by_slide.items():
        m = slide == sid
        idx = np.where(m)[0]
        remap = {g: k for k, g in enumerate(idx)}
        vals.append(fide_score(labels[m], (np.array([remap[k] for k in idx[i]]),
                                           np.array([remap[k] for k in idx[j]])),
                               n_classes=n_classes))
    return float(np.mean(vals))


def jensen_shannon_divergence(labels: np.ndarray, slide: np.ndarray) -> float:
    """跨切片域分布 JSD。越低 = 域在切片间越一致(可迁移), 上游 _jensen_shannon_divergence。"""
    cats = sorted(set(labels))
    dists = []
    for sid in sorted(set(slide)):
        v = pd.Series(labels[slide == sid]).value_counts()
        dists.append(np.array([v.get(c, 0) for c in cats], dtype=float))
    D = np.array(dists)
    D = D / D.sum(1)[:, None]
    return float(_entropy(D.mean(0)) - np.mean([_entropy(d) for d in D]))


def heuristic(labels: np.ndarray, edges_by_slide: dict, slide: np.ndarray,
              n_classes: int) -> float:
    """上游 heuristic(): 对每张切片各算 fide * entropy(该片域占比) / log2(n_classes), 再跨片平均。

    严格对齐 novae/monitor/eval.py 的 heuristic()/_heuristic() —— 上游是**逐切片**算完整式子
    再取均值(mean over _heuristic per slide), 不是"平均 FIDE × 全局 entropy"。
    """
    vals = []
    for sid, (i, j) in edges_by_slide.items():
        m = slide == sid
        idx = np.where(m)[0]
        remap = {g: k for k, g in enumerate(idx)}
        fide_s = fide_score(labels[m], (np.array([remap[k] for k in idx[i]]),
                                        np.array([remap[k] for k in idx[j]])),
                            n_classes=n_classes)
        p = pd.Series(labels[m]).value_counts(normalize=True).values
        p = np.pad(p, (0, max(0, n_classes - len(p))))
        vals.append(fide_s * _entropy(p) / np.log2(n_classes))
    return float(np.mean(vals))


