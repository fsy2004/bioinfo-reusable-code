# ---------------------------------------------------------------------------
# 合成示例数据: 3 张切片, 共享同一套真实空间域, 但各自带切片特异的批次漂移。
#
# ★ 关键设计(否则这个 demo 就不是 niche 问题): **域不是由单细胞表达定义的**, 而是由
#   邻域内的细胞类型**组成比例**定义的 —— 4 种细胞类型在所有域中都出现, 只是比例不同。
#   因此单看一个细胞的表达只能还原它的细胞类型, 还原不了它属于哪个空间域; 必须看邻域。
#   这正是 Novae 这类 niche 基础模型要解决的问题, 也让 expression-only 成为真实的地板。
# ---------------------------------------------------------------------------
def make_example(path: Path, n_slides: int = 3, per_side: int = 18,
                 n_genes: int = 40, seed: int = SEED) -> pd.DataFrame:
    rng = np.random.default_rng(seed)
    n_dom, n_ct = 4, 4
    # 细胞类型 -> 基因程序(表达只由细胞类型决定)
    programs = rng.gamma(2.0, 1.0, size=(n_ct, n_genes))
    # 域 -> 细胞类型组成: 主导型占 55%, 其余各 15%
    comp = np.full((n_dom, n_ct), 0.15)
    comp[np.arange(n_dom), np.arange(n_ct)] = 0.55
    comp = comp / comp.sum(1, keepdims=True)
    rows = []
    for s in range(n_slides):
        gx, gy = np.meshgrid(np.arange(per_side), np.arange(per_side))
        x = gx.ravel() + rng.normal(0, 0.18, gx.size)      # 抖动, 避免完美格点
        y = gy.ravel() + rng.normal(0, 0.18, gy.size)
        # 真实域: 左右条带 x 上下条带 -> 4 个空间连续的区块
        dom = (gx.ravel() >= per_side / 2).astype(int) * 2 + (gy.ravel() >= per_side / 2).astype(int)
        # 细胞类型按所在域的组成比例抽样(域内混杂, 这是 niche 的本质)
        ct = np.array([rng.choice(n_ct, p=comp[d]) for d in dom])
        mu = programs[ct]
        batch = rng.lognormal(0.0, 0.35, size=(1, n_genes))   # 切片特异乘性批次效应
        counts = rng.poisson(np.clip(mu * batch, 0.05, None))
        df = pd.DataFrame(counts, columns=[f"G{i:03d}" for i in range(n_genes)])
        df.insert(0, "cell_type_true", [f"CT{c}" for c in ct])
        df.insert(0, "domain_true", [f"D{d}" for d in dom])
        df.insert(0, "y", np.round(y, 3))
        df.insert(0, "x", np.round(x, 3))
        df.insert(0, "slide", f"slide{s + 1}")
        df.insert(0, "cell_id", [f"s{s + 1}_c{i:04d}" for i in range(len(df))])
        rows.append(df)
    out = pd.concat(rows, ignore_index=True)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", newline="", encoding="utf-8") as fh:
        fh.write("# synthetic, for demo only -- generated by 580_novae_spatial_fm.py\n")
        out.to_csv(fh, index=False)
    return out


def load_table(path: Path) -> pd.DataFrame:
    return pd.read_csv(path, comment="#")


