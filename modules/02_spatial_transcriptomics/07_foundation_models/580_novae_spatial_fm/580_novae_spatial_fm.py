def _spatial_edges(xy: np.ndarray, k: int = 6):
    """切片内空间 kNN 图, 返回对称化的 (i, j) 边列表。"""
    from sklearn.neighbors import kneighbors_graph
    A = kneighbors_graph(xy, n_neighbors=min(k, len(xy) - 1), mode="connectivity")
    A = ((A + A.T) > 0).astype(int)
    return A.nonzero()


def _normalize_log(X: np.ndarray) -> np.ndarray:
    """CPM-style 归一 + log1p(scanpy 默认前处理的等价最小实现)。"""
    tot = X.sum(1, keepdims=True)
    tot[tot == 0] = 1.0
    return np.log1p(X / tot * np.median(tot))


def baseline_expression_only(X: np.ndarray, n_domains: int, seed: int = SEED) -> np.ndarray:
    """地板 A: 完全忽略空间坐标, PCA + KMeans。空间模型若打不过它, 空间信息就没用上。"""
    from sklearn.cluster import KMeans
    from sklearn.decomposition import PCA
    Z = PCA(n_components=min(20, X.shape[1] - 1), random_state=seed).fit_transform(X)
    lab = KMeans(n_clusters=n_domains, n_init=20, random_state=seed).fit_predict(Z)
    return np.array([f"E{c}" for c in lab])


def baseline_niche_smoothing(X: np.ndarray, xy: np.ndarray, slide: np.ndarray,
                             n_domains: int, k: int = 20, seed: int = SEED,
                             center_per_slide: bool = False, prefix: str = "N") -> np.ndarray:
    """地板 B: 空间 kNN 邻域内平均表达(经典 niche 构造) -> PCA + KMeans。

    这是空间域划分最朴素但很难打败的对照 —— 任何 niche 基础模型的增益都应相对它衡量。

    center_per_slide=True 时先做**逐切片 z-score**, 这是最朴素的批次处理(Harmony 的
    穷人版)。不加这一步时, 邻域平均会把细胞类型噪声抹掉、反而让切片批次成为主方差方向,
    KMeans 就会按切片而不是按域聚类 —— 本模块默认把两个变体都跑出来, 让这件事看得见。
    """
    from sklearn.cluster import KMeans
    from sklearn.decomposition import PCA
    from sklearn.neighbors import kneighbors_graph
    S = np.zeros_like(X)
    for sid in np.unique(slide):
        m = slide == sid
        A = kneighbors_graph(xy[m], n_neighbors=min(k, m.sum() - 1), mode="connectivity")
        A = A + np.eye(m.sum())          # 含自身
        S[m] = np.asarray(A @ X[m]) / np.asarray(A.sum(1))
        if center_per_slide:             # 逐切片 z-score, 消掉切片特异的均值/尺度漂移
            sd = S[m].std(0); sd[sd == 0] = 1.0
            S[m] = (S[m] - S[m].mean(0)) / sd
    Z = PCA(n_components=min(20, X.shape[1] - 1), random_state=seed).fit_transform(S)
    lab = KMeans(n_clusters=n_domains, n_init=20, random_state=seed).fit_predict(Z)
    return np.array([f"{prefix}{c}" for c in lab])


