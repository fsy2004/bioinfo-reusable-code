def _gaussian_field(coords, n_prog, rng, n_bumps=12, width=220.0):
    """用随机高斯基函数叠加出空间平滑的潜在组织程序活性 (n_cells x n_prog)。

    这样合成的表达谱有真实的空间结构(域/梯度),而不是纯噪声;
    H&E 与组学共享同一批潜在程序 —— 这正是"组织学锚定"能成立的前提。
    """
    n = coords.shape[0]
    z = np.zeros((n, n_prog))
    lo, hi = coords.min(0), coords.max(0)
    for p in range(n_prog):
        for _ in range(n_bumps):
            c = rng.uniform(lo, hi)
            amp = rng.normal(0, 1)
            d2 = ((coords - c) ** 2).sum(1)
            z[:, p] += amp * np.exp(-d2 / (2 * width ** 2))
    return (z - z.mean(0)) / (z.std(0) + 1e-8)


def _make_slice(n_cells, W_he, W_a, W_b, gain_a, gain_b, rng, field_size=1500.0):
    """生成一张切片:空间坐标 + H&E embedding + panel A + panel B(B 部分留作真值)。"""
    n_prog = W_he.shape[0]
    coords = rng.uniform(0, field_size, size=(n_cells, 2))
    z = _gaussian_field(coords, n_prog, rng)

    # H&E embedding:形态学表征 = 潜在程序的投影 + 相当量的形态学专属噪声
    he = z @ W_he + rng.normal(0, 0.9, size=(n_cells, W_he.shape[1]))

    def _panel(W, gain):
        # gain 逐基因不同 → 有的基因强依赖形态(好预测),有的几乎不依赖(难预测)
        lam = np.exp((z @ W) * gain[None, :] * 0.8 + 1.0)
        counts = rng.poisson(np.clip(lam, 1e-3, 200.0))
        return np.log1p(counts)                       # 与 scanpy 常规 log1p 归一一致

    return coords, he, _panel(W_a, gain_a), _panel(W_b, gain_b)


def generate_example_data(outdir=EXAMPLE, n_cells=700, n_prog=10, he_dim=64,
                          n_genes=30, seed=SEED):
    """写出 example_data/ 的合成 CSV。两张切片共享 W(同种组织的两张连续切片)。"""
    os.makedirs(outdir, exist_ok=True)
    rng = np.random.default_rng(seed)
    W_he = rng.normal(0, 1, (n_prog, he_dim))
    W_a = rng.normal(0, 1, (n_prog, n_genes))
    W_b = rng.normal(0, 1, (n_prog, n_genes))
    gain_a = rng.uniform(0.15, 1.4, n_genes)          # 逐基因形态-耦合强度
    gain_b = rng.uniform(0.15, 1.4, n_genes)

    genes_a = [f"PanelA_{i:02d}" for i in range(n_genes)]
    genes_b = [f"PanelB_{i:02d}" for i in range(n_genes)]
    he_cols = [f"HE{i:03d}" for i in range(he_dim)]

    for s in (1, 2):
        cells = [f"S{s}_C{i:04d}" for i in range(n_cells)]
        coords, he, A, B = _make_slice(n_cells, W_he, W_a, W_b, gain_a, gain_b, rng)
        frames = {
            f"slice{s}_coords.csv": pd.DataFrame(coords, index=cells, columns=["x", "y"]),
            f"slice{s}_he.csv": pd.DataFrame(he, index=cells, columns=he_cols),
            f"slice{s}_panelA.csv": pd.DataFrame(A, index=cells, columns=genes_a),
            f"slice{s}_panelB.csv": pd.DataFrame(B, index=cells, columns=genes_b),
        }
        for fname, df in frames.items():
            df.index.name = "cell_id"
            path = os.path.join(outdir, fname)
            with open(path, "w", encoding="utf-8", newline="") as fh:
                fh.write(HEADER)
                df.round(4).to_csv(fh)
    print(f"[578] example_data written to {outdir}")


