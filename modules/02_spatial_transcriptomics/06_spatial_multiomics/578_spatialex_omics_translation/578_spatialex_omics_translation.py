    import sklearn
    import matplotlib
    session = {"python": platform.python_version(), "numpy": np.__version__,
               "pandas": pd.__version__, "scikit-learn": sklearn.__version__,
               "matplotlib": matplotlib.__version__, "platform": platform.platform()}

    with open(os.path.join(a.outdir, "578_summary.json"), "w", encoding="utf-8") as fh:
        json.dump({"baseline_metrics": results, "spatialex": up,
                   "params": {"ridge_alpha": a.ridge_alpha, "knn": a.knn,
                              "smooth_alpha": a.smooth_alpha, "seed": SEED},
                   "session_info": session},
                  fh, indent=1, ensure_ascii=False, default=str)
    print(f"[578] done · {os.path.join(a.outdir, '578_summary.json')}")


