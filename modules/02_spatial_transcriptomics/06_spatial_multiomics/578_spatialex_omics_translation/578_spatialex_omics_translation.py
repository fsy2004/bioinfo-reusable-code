# ---------------------------------------------------------------------------
# 2. 评估指标(沿用上游 utils.Compute_metrics 支持的量:PCC / cosine / CMD)
#    上游定义在 SpatialEx/utils.py:63;PCC 与 CMD 口径与上游一致,
#    cosine 则是本模块自选的逐细胞视角(上游按 axis=0 逐基因算)。
# ---------------------------------------------------------------------------
def per_gene_pcc(true, pred):
    """逐基因 Pearson r。恒定列返回 nan(不当 0 混进均值)。"""
    t = true - true.mean(0, keepdims=True)
    p = pred - pred.mean(0, keepdims=True)
    num = (t * p).sum(0)
    den = np.sqrt((t ** 2).sum(0) * (p ** 2).sum(0))
    out = np.full(true.shape[1], np.nan)
    ok = den > 1e-12
    out[ok] = num[ok] / den[ok]
    return out


def per_cell_cosine(true, pred):
    """逐细胞 cosine similarity(整条表达向量的形状是否对)。

    注意:上游 Compute_metrics(metric='cosine_similarity') 是按 axis=0 逐**基因**算的,
    这里按 axis=1 逐**细胞**算,属本模块自选的补充视角,不要当成上游口径引用。
    """
    num = (true * pred).sum(1)
    den = np.linalg.norm(true, axis=1) * np.linalg.norm(pred, axis=1)
    return np.where(den > 1e-12, num / (den + 1e-12), np.nan)


def cmd(true, pred):
    """Cosine Matrix Distance:比较真值与预测的**基因-基因相关结构**。

    CMD = 1 - tr(R1 R2) / (||R1||_F ||R2||_F),0 = 共表达结构完全一致。
    这一项抓的是逐基因 PCC 抓不到的东西:模型是否重建了基因间的协同关系。
    """
    R1 = np.nan_to_num(np.corrcoef(true, rowvar=False))
    R2 = np.nan_to_num(np.corrcoef(pred, rowvar=False))
    denom = np.linalg.norm(R1) * np.linalg.norm(R2)
    return float(1 - np.trace(R1 @ R2) / denom) if denom > 1e-12 else np.nan


# ---------------------------------------------------------------------------
# 3. 基线:H&E → omics 的岭回归(+ 空间 kNN 平滑),对照全局均值地板
# ---------------------------------------------------------------------------
def spatial_smooth(pred, coords, k=7, alpha=0.5):
    """把预测在空间 kNN 邻域上平滑,近似上游超图的邻域聚合。

    alpha=0 即不平滑;alpha=1 即完全用邻域均值。默认 0.5 = 自身与邻域各半。
    这里只平滑**预测值**,不碰真值 —— 避免把答案泄漏进特征。
    """
    from sklearn.neighbors import NearestNeighbors
    k = int(min(k + 1, coords.shape[0]))
    nn = NearestNeighbors(n_neighbors=k).fit(coords)
    idx = nn.kneighbors(coords, return_distance=False)     # 第 0 列是自身
    neigh_mean = pred[idx].mean(axis=1)
    return (1 - alpha) * pred + alpha * neigh_mean


def translate_one_direction(he_train, y_train, he_test, y_test, coords_test,
                            alpha_ridge=10.0, k=7, alpha_smooth=0.5):
    """在 slice_train 上学 H&E→panel,跨切片应用到 slice_test 上,与真值比。

    ★ 无数据泄漏:训练完全发生在 train 切片,test 切片的表达真值只在评估时读取。
    ★ 跨切片评估(而非切片内 CV)才是上游宣称的能力:把 panel 搬到没测过它的切片上。
    """
    from sklearn.linear_model import Ridge
    from sklearn.preprocessing import StandardScaler

    sc = StandardScaler().fit(he_train)
    model = Ridge(alpha=alpha_ridge, random_state=None)
    model.fit(sc.transform(he_train), y_train)

    preds = {
        # 地板:不看 H&E,一律预测训练切片的基因均值
        "Mean floor": np.tile(y_train.mean(0), (he_test.shape[0], 1)),
        "Ridge (H&E)": model.predict(sc.transform(he_test)),
    }
    preds["Ridge + spatial kNN"] = spatial_smooth(
        preds["Ridge (H&E)"], coords_test, k=k, alpha=alpha_smooth)

    metrics = {}
    for name, P in preds.items():
        g = per_gene_pcc(y_test, P)
        metrics[name] = {
            "median_gene_PCC": float(np.nanmedian(g)),
            "mean_gene_PCC": float(np.nanmean(g)),
            "mean_cell_cosine": float(np.nanmean(per_cell_cosine(y_test, P))),
            "CMD": cmd(y_test, P),
        }
    gene_pcc = pd.DataFrame({n: per_gene_pcc(y_test, P) for n, P in preds.items()})
    return preds, metrics, gene_pcc


