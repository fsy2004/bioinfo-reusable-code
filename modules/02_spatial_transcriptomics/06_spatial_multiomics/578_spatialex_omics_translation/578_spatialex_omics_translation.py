    results, gene_pcc_store, preds_store = {}, {}, {}
    jobs = [
        ("panelA_slice1_to_slice2", he1.values, A1.values, he2.values, A2.values,
         c2.values, list(A1.columns)),
        ("panelB_slice2_to_slice1", he2.values, B2.values, he1.values, B1.values,
         c1.values, list(B1.columns)),
    ]
    for name, hetr, ytr, hete, yte, cte, genes in jobs:
        preds, met, gp = translate_one_direction(
            hetr, ytr, hete, yte, cte,
            alpha_ridge=a.ridge_alpha, k=a.knn, alpha_smooth=a.smooth_alpha)
        results[name] = met
        gene_pcc_store[name] = gp
        preds_store[name] = (preds, yte, cte, genes)
        print(f"       [{name}]")
        for m, v in met.items():
            print(f"         {m:22s} medPCC={v['median_gene_PCC']:+.3f} "
                  f"cellCos={v['mean_cell_cosine']:.3f} CMD={v['CMD']:.3f}")

    print("[578] Step 3 · 写出指标表")
    pd.concat({k: pd.DataFrame(v).T for k, v in results.items()}, names=["direction", "method"]) \
        .to_csv(os.path.join(a.outdir, "578_metrics.csv"))
    for name, gp in gene_pcc_store.items():
        gp.index = preds_store[name][3]
        gp.to_csv(os.path.join(a.outdir, f"578_gene_pcc_{name}.csv"))

    print("[578] Step 4 · 出图(dumbbell / 散点 / 空间图 / heatmap)")
    main_dir = "panelB_slice2_to_slice1"
    preds, y_true, coords, genes = preds_store[main_dir]
    gp = gene_pcc_store[main_dir].reset_index(drop=True)
    for stem, fn in [
        ("578_gene_pcc_dumbbell", lambda p: fig_dumbbell(gp, genes, p)),
        ("578_pred_vs_measured_scatter", lambda p: fig_scatter(y_true, preds, gp, genes, p)),
        ("578_spatial_translation", lambda p: fig_spatial(coords, y_true, preds, gp, genes, p)),
        ("578_coexpression_heatmap", lambda p: fig_corr_heatmap(y_true, preds, p)),
    ]:
        fn(os.path.join(ASSETS, stem))
        print(f"       assets/{stem}.png (+ .pdf)")

    print("[578] Step 5 · SpatialEx 上游路径")
    up = run_spatialex() if a.run_spatialex else {
        "status": "not requested", "hint": "加 --run-spatialex 检测上游可用性(需装包 + GPU)"}
    for k, v in up.items():
        print(f"       {k}: {v}" if not isinstance(v, list) else f"       {k}: {len(v)} items")

