def fig_dumbbell(gene_pcc, genes, outstem):
    """Dumbbell:逐基因 PCC 从"均值地板"到"Ridge"再到"Ridge+空间"的移动。"""
    import matplotlib.pyplot as plt
    order = gene_pcc["Ridge + spatial kNN"].sort_values().index
    y = np.arange(len(order))
    c = pal(3, "npg")
    fig, ax = plt.subplots(figsize=(NATURE_W1 * 1.6, 0.16 * len(order) + 1.4))
    a = gene_pcc.loc[order, "Ridge (H&E)"].values
    b = gene_pcc.loc[order, "Ridge + spatial kNN"].values
    for yi, ai, bi in zip(y, a, b):
        ax.plot([ai, bi], [yi, yi], color="#BBBBBB", lw=1.0, zorder=1)
    ax.scatter(gene_pcc.loc[order, "Mean floor"], y, s=16, color=c[2],
               label="Mean floor", zorder=2)
    ax.scatter(a, y, s=22, color=c[1], label="Ridge (H&E)", zorder=3)
    ax.scatter(b, y, s=22, color=c[0], label="Ridge + spatial kNN", zorder=3)
    ax.axvline(0, color="black", lw=0.8, ls="--")
    ax.set_yticks(y)
    ax.set_yticklabels([genes[i] for i in order], fontsize=6)
    ax.set_xlabel("Per-gene Pearson r (cross-slice held-out)")
    ax.set_title("Histology-anchored panel translation")
    ax.legend(loc="lower right", fontsize=7)
    save_fig(fig, outstem)
    plt.close(fig)


def fig_scatter(y_true, preds, gene_pcc, genes, outstem, n_show=4):
    """散点:预测 vs 真值,取 PCC 分位数上的代表性基因(不只挑最好的)。"""
    import matplotlib.pyplot as plt
    r = gene_pcc["Ridge + spatial kNN"]
    picks = [r.sort_values().index[int(q * (len(r) - 1))] for q in (0.15, 0.5, 0.8, 0.98)][:n_show]
    fig, axes = plt.subplots(1, n_show, figsize=(NATURE_W2, 2.1), constrained_layout=True)
    P = preds["Ridge + spatial kNN"]
    for ax, gi in zip(np.ravel(axes), picks):
        ax.scatter(y_true[:, gi], P[:, gi], s=5, alpha=0.35,
                   color=pal(3, "npg")[0], edgecolors="none", rasterized=True)
        lim = [min(y_true[:, gi].min(), P[:, gi].min()), max(y_true[:, gi].max(), P[:, gi].max())]
        ax.plot(lim, lim, color="black", lw=0.8, ls="--")
        ax.set_title(f"{genes[gi]}\nr = {r[gi]:.2f}", fontsize=7)
        ax.set_xlabel("Measured", fontsize=7)
    np.ravel(axes)[0].set_ylabel("Predicted from H&E", fontsize=7)
    save_fig(fig, outstem)
    plt.close(fig)


def fig_spatial(coords, y_true, preds, gene_pcc, genes, outstem):
    """空间图:同一基因的真值 / Ridge / Ridge+空间 三联,看空间结构是否被还原。"""
    import matplotlib.pyplot as plt
    gi = int(gene_pcc["Ridge + spatial kNN"].idxmax())
    panels = [("Measured", y_true[:, gi])] + \
             [(k, preds[k][:, gi]) for k in ("Ridge (H&E)", "Ridge + spatial kNN")]
    vmin, vmax = np.percentile(np.concatenate([p for _, p in panels]), [2, 98])
    fig, axes = plt.subplots(1, 3, figsize=(NATURE_W2, 2.5), constrained_layout=True)
    for ax, (name, v) in zip(axes, panels):
        sm = ax.scatter(coords[:, 0], coords[:, 1], c=v, s=6, cmap=CMAP_CONT,
                        vmin=vmin, vmax=vmax, edgecolors="none", rasterized=True)
        ax.set_title(name, fontsize=8)
        ax.set_aspect("equal")
        ax.set_xticks([]); ax.set_yticks([])
        for sp in ax.spines.values():
            sp.set_visible(False)
    fig.colorbar(sm, ax=axes, shrink=0.7, label=f"{genes[gi]} (log1p)")
    save_fig(fig, outstem)
    plt.close(fig)


def fig_corr_heatmap(y_true, preds, outstem):
    """Heatmap:真值 vs 预测的基因-基因相关结构(CMD 度量的就是这两块的差)。"""
    import matplotlib.pyplot as plt
    P = preds["Ridge + spatial kNN"]
    R1 = np.nan_to_num(np.corrcoef(y_true, rowvar=False))
    R2 = np.nan_to_num(np.corrcoef(P, rowvar=False))
    o = np.argsort(R1.mean(0))
    fig, axes = plt.subplots(1, 2, figsize=(NATURE_W2 * 0.8, 3.1), constrained_layout=True)
    for ax, M, t in zip(axes, (R1[np.ix_(o, o)], R2[np.ix_(o, o)]),
                        ("Measured gene-gene r", "Predicted gene-gene r")):
        im = ax.imshow(M, cmap=CMAP_DIVERGE, vmin=-1, vmax=1)
        ax.set_title(t, fontsize=8)
        ax.set_xticks([]); ax.set_yticks([])
    fig.colorbar(im, ax=axes, shrink=0.7, label="Pearson r")
    fig.suptitle(f"Co-expression structure  ·  CMD = {cmd(y_true, P):.3f}", fontsize=9)
    save_fig(fig, outstem)
    plt.close(fig)


