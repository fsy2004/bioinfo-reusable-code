def main():
    ap = argparse.ArgumentParser(description="577 SPIDER-style interface-level SVI + baseline")
    ap.add_argument("--datadir", default=EXAMPLE, help="含 expression.csv/spot_meta.csv/lr_pairs.csv")
    ap.add_argument("--outdir", default=RESULTS)
    ap.add_argument("--max-degree", type=int, default=6, help="每个 spot 最多保留的 interface 数")
    ap.add_argument("--n-perm", type=int, default=999)
    ap.add_argument("--k-neighbors", type=int, default=6)
    ap.add_argument("--seed", type=int, default=SEED)
    ap.add_argument("--run-spider", action="store_true", help="走官方 spider-st 包(需安装)")
    ap.add_argument("--adata", default=None, help="官方路径的 .h5ad 输入")
    ap.add_argument("--r-path", default="", help="官方 find_svi 的 R_path")
    ap.add_argument("--cluster-key", default=None,
                    help="官方 prep 的 cluster_key(缺省读 adata.uns,再缺省用官方默认 'type')")
    ap.add_argument("--is-human", dest="is_human", action="store_true", default=None)
    ap.add_argument("--is-mouse", dest="is_human", action="store_false",
                    help="官方 prep 的 is_human=False")
    ap.add_argument("--is-sc", dest="is_sc", action="store_true", default=None,
                    help="官方 prep 的 is_sc=True(单细胞分辨率数据)")
    a = ap.parse_args()
    os.makedirs(a.outdir, exist_ok=True)

    if not os.path.exists(os.path.join(a.datadir, "expression.csv")):
        print("[577] Step 0 · 生成合成示例数据")
        make_synthetic(a.datadir, seed=a.seed)
    meta, expr, lr, truth = load_data(a.datadir)
    print(f"[577] Step 1 · 载入 {len(meta)} spots × {expr.shape[1]} genes, {len(lr)} LR pairs")

    idf = build_interfaces(meta, max_degree=a.max_degree)
    print(f"[577] Step 2 · 构建 {len(idf)} 条 interface(容量约束 max_degree={a.max_degree})")

    res, if_scores = score_and_test(meta, expr, lr, idf, n_perm=a.n_perm,
                                    k_spot=a.k_neighbors, k_iface=a.k_neighbors, seed=a.seed)
    print(f"[577] Step 3 · Moran's I 置换检验({a.n_perm} perms)+ BH 校正")

    if truth:
        res["truth"] = res["lr_pair"].map(truth).fillna("unknown")
        pos = res["truth"].isin(["interface_SVI", "coexpressed_SVI"])
        rec = {"interface_level_recall": float(((res["iface_fdr"] < .05) & pos).sum() / pos.sum()),
               "spot_level_recall": float(((res["spot_fdr"] < .05) & pos).sum() / pos.sum()),
               "interface_level_FP": int(((res["iface_fdr"] < .05) & ~pos).sum()),
               "spot_level_FP": int(((res["spot_fdr"] < .05) & ~pos).sum())}
        print("[577] Step 4 · 对照(合成 ground truth):")
        for k, v in rec.items():
            print(f"        {k}: {v}")
    else:
        rec = {"note": "no ground_truth.json in datadir; recall not computed"}

    idf.to_csv(os.path.join(a.outdir, "interfaces.csv"), index=False)
    res.to_csv(os.path.join(a.outdir, "svi_results.csv"), index=False)
    print("[577] Step 5 · 出图")
    figures(meta, idf, res, if_scores, a.outdir)

    summary = {"n_spots": int(len(meta)), "n_interfaces": int(len(idf)),
               "n_lr_pairs": int(len(res)), "seed": a.seed, "n_perm": a.n_perm,
               "benchmark_vs_synthetic_truth": rec,
               "top_interface_SVI": res.head(5)[["lr_pair", "iface_moranI", "iface_fdr"]]
                                       .to_dict("records")}
    if a.run_spider:
        print("[577] Step 6 · 官方 SPIDER 路径")
        summary["official_spider"] = run_official_spider(
            a.adata, a.outdir, a.r_path,
            cluster_key=a.cluster_key, is_human=a.is_human, is_sc=a.is_sc)
        print(f"        {summary['official_spider']}")
    else:
        summary["official_spider"] = {"status": "not requested (--run-spider)"}

    with open(os.path.join(a.outdir, "577_summary.json"), "w") as fh:
        json.dump(summary, fh, indent=1, default=str)

    # 依赖版本快照(铁律6:可复现)
    import platform
    import scipy, sklearn, matplotlib
    with open(os.path.join(a.outdir, "versions.txt"), "w") as fh:
        fh.write(f"python\t{platform.python_version()}\nplatform\t{platform.platform()}\n")
        for m in (np, pd, scipy, sklearn, matplotlib):
            fh.write(f"{m.__name__}\t{getattr(m, '__version__', '?')}\n")
        try:
            import spider
            fh.write(f"spider\t{getattr(spider, '__version__', 'installed')}\n")
        except ImportError:
            fh.write("spider\tNOT INSTALLED (official path unavailable)\n")
    print(f"[577] done → {a.outdir}")


if __name__ == "__main__":
    main()
