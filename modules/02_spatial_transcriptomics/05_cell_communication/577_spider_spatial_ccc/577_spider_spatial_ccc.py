def run_official_spider(adata_path, out_f, r_path, cluster_key=None, is_human=None, is_sc=None):
    """守卫式封装:调用序列取自官方 README,并已逐个比对官方源码签名。

    核对来源(2026-07-20 抓取,非仅凭 README):
      spider/__init__.py            : `from .SPIDER import SPIDER`  → `import spider; spider.SPIDER()`
      spider/SPIDER.py  SPIDER.prep(adata_input, cluster_key='type', is_human=True, cutoff=None,
                                    imputation=False, itermax=1000, lr_raw=None, pathway_raw=None,
                                    is_sc=False, normalize_total=False) -> idata
      spider/SPIDER.py  SPIDER.find_svi(idata, out_f, R_path, abstract=True, overwrite=False,
                                    n_neighbors=5, alpha=0.3, threshold=0.01, ...,
                                    svi_number=10, n_jobs=10) -> (idata, meta_idata)
      spider/svi.py     tf_corr(idata, adata, is_human, out_f, threshold=0.3, ...)
      spider/svi.py     combine_SVI(idata, threshold, svi_number=10) -> (svi_df, svi_df_strict)

    ★ out_f 必须以路径分隔符结尾:官方全程用 f-string 直接拼接,不用 os.path.join。
      实测计数(2026-07-21 对本地克隆源码):spider/svi.py 内 f'{out_f}...' 13 处
      (如 svi.py:118 f'{out_f}sparse_tf_corr.npz'),另 f'{work_dir}...' 35 处
      —— find_svi(SPIDER.py:180)把 out_f 按位置传给 svi.find_svi(svi.py:386)的
      work_dir,再传给 svi_moran/svi_nnSVG 等;SPIDER.py 内另有 f'{out_f}pattern.csv'
      等 13 处(SPIDER.py:170-188),util.py 1 处 —— 全仓 f'{out_f}' 合计 27 处。
      全仓 os.path.join 仅 5 处,全在 vendored
      SpatialDE2/io.py(spaceranger 读图),SPIDER 核心模块 0 处。
      不带分隔符会把文件写到上级目录、并让 find_svi 的 `mkdir(out_f)`
      (SPIDER.py:155,159,非递归)建出错目录。此处统一补上。

    未安装则返回真实安装命令,不做任何静默替代。
    """
    try:
        import spider  # noqa: F401
    except ImportError:
        return {"status": "skipped",
                "reason": "package `spider` (PyPI: spider-st) not installed",
                "install": ["conda create -n spider python=3.8",
                            "conda install -c conda-forge somoclu fa2",
                            "pip install Cython",
                            "SKLEARN_ALLOW_DEPRECATED_SKLEARN_PACKAGE_INSTALL=TRUE pip install sklearn",
                            "pip install scgco",
                            "pip install spider-st"],
                "no_install_option": "CodeOcean capsule https://codeocean.com/capsule/1194038/tree/v1"}
    if adata_path is None:
        return {"status": "skipped",
                "reason": "official path needs --adata (an .h5ad). cluster_key/is_human/is_sc "
                          "由 --cluster-key/--is-human/--is-sc 指定;未指定则回落到 adata.uns "
                          "同名键(官方 README 的写法),再没有就用官方源码默认值 "
                          "cluster_key='type', is_human=True, is_sc=False"}
    import anndata
    adata = anndata.read_h5ad(adata_path)

    # README 的 Quick Start 直接读 adata.uns['cluster_key'] 等键,但这三个键并非 h5ad 标准内容,
    # 缺失即 KeyError。故:命令行 > adata.uns > 官方源码签名里的默认值。
    ck = cluster_key if cluster_key else adata.uns.get("cluster_key", "type")
    hum = is_human if is_human is not None else bool(adata.uns.get("is_human", True))
    sc_ = is_sc if is_sc is not None else bool(adata.uns.get("is_sc", False))

    out_f = out_f.rstrip("/\\") + os.sep          # 官方按 f'{out_f}xxx.csv' 拼接,必须带分隔符
    os.makedirs(out_f, exist_ok=True)             # 官方 find_svi 用的是非递归 mkdir

    op = spider.SPIDER()
    idata = op.prep(adata, cluster_key=ck, is_human=hum, is_sc=sc_,
                    itermax=1000, imputation=True, normalize_total=True)
    op.svi.tf_corr(idata, adata, hum, out_f, threshold=0.3)
    idata, meta_idata = op.find_svi(idata, out_f, r_path, alpha=0.3, overwrite=True,
                                    n_jobs=1, svi_number=0)
    svi_df, svi_df_strict = op.svi.combine_SVI(idata, threshold=0.01)
    svi_df.to_csv(os.path.join(out_f, "spider_svi.csv"))
    svi_df_strict.to_csv(os.path.join(out_f, "spider_svi_strict.csv"))
    return {"status": "ok", "out_f": out_f,
            "n_svi": int(len(svi_df)), "n_svi_strict": int(len(svi_df_strict))}


